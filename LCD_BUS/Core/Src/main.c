// Luu 99 tuyen o dia chi 0.  99 x 5 / 256 ~~ 2 page 
void Save_All_Tuyens_To_Flash_Clean(void)
{
    char all_ma[TUYEN_TOTAL_COUNT * TUYEN_SIZE];
    for (uint8_t i = 0; i < TUYEN_TOTAL_COUNT; i++) {
        snprintf(&all_ma[i * TUYEN_SIZE], TUYEN_SIZE, "%03dR3", i + 1);
    }
    W25Q_Write_Clean(0, 0, sizeof(all_ma), (uint8_t*)all_ma);
}


